export function formatCodeContext(
    relevantCode: Array<{ content: string; filePath: string }>
): string {
    if (relevantCode.length === 0) {
        return "No additional code context available.";
    }

    return relevantCode
        .map(
            (chunk) =>
                `### ${chunk.filePath}\n\`\`\`\n${chunk.content}\n\`\`\``
        )
        .join("\n\n");
}

export function formatReviewContext(
    pastReviews: Array<{ content: string; prTitle: string }>
): string {
    if (pastReviews.length === 0) {
        return "No past review context available.";
    }

    return pastReviews
        .map((review) => `### PR: ${review.prTitle}\n${review.content}`)
        .join("\n\n");
}

export function formatReviewMarkdown(review: {
    riskScore: number;
    riskLevel: string;
    riskFactors: string[];
    blastRadius: { filesAffected: number; servicesAffected: string[] };
    deploymentRecommendations: string[];
    codeObservations: string[];
    retrieval?: RetrievalMeta;
}): string {
    const riskEmoji =
        review.riskLevel === "Critical"
            ? "🔴"
            : review.riskLevel === "High"
              ? "🟠"
              : review.riskLevel === "Medium"
                ? "🟡"
                : "🟢";

    const retrievalWarning =
        review.retrieval?.status === "degraded"
            ? `> Context retrieval degraded for this run. DeployIQ generated the report with partial or unavailable repository context.\n\n`
            : "";

    return `## DeployIQ Risk Assessment

${riskEmoji} **Risk Score: ${review.riskScore}/10** (${review.riskLevel})

${retrievalWarning}### Risk Factors
${review.riskFactors.map((f) => `- ${f}`).join("\n")}

### Blast Radius
Files affected: ${review.blastRadius.filesAffected} | Services affected: ${review.blastRadius.servicesAffected.join(", ") || "None identified"}

### Deployment Recommendations
${review.deploymentRecommendations.map((r, i) => `${i + 1}. ${r}`).join("\n")}

### Code Observations
${review.codeObservations.map((o) => `- ${o}`).join("\n")}

---
*Generated by [DeployIQ](https://deployiq.dev) — deployment risk analysis, not code review.*`;
}
